package com.sun3d.culturalShanghai.handler;

public class CodeInfo {
	String date;
	String name;
	int changeType;
	String description;
	int integralChange;
	int integralNow;
	int status;

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getChangeType() {
		return changeType;
	}

	public void setChangeType(int changeType) {
		this.changeType = changeType;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public int getIntegralChange() {
		return integralChange;
	}

	public void setIntegralChange(int integralChange) {
		this.integralChange = integralChange;
	}

	public int getIntegralNow() {
		return integralNow;
	}

	public void setIntegralNow(int integralNow) {
		this.integralNow = integralNow;
	}

	public int getStatus() {
		return status;
	}

	public void setStatus(int status) {
		this.status = status;
	}

}
