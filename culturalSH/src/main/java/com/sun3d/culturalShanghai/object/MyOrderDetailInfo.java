package com.sun3d.culturalShanghai.object;

public class MyOrderDetailInfo {

	String left_str;
	String right_str;

	public String getLeft_str() {
		return left_str;
	}

	public void setLeft_str(String left_str) {
		this.left_str = left_str;
	}

	public String getRight_str() {
		return right_str;
	}

	public void setRight_str(String right_str) {
		this.right_str = right_str;
	}

	public MyOrderDetailInfo(String left_str, String right_str) {
		super();
		this.left_str = left_str;
		this.right_str = right_str;
	}

}
